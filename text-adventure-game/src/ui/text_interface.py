def display_text(text):
    """
    Display a string of text to the user.

    Args:
        text (str): The text to display.
    """
    print(text)

def get_user_input(prompt):
    """
    Get input from the user.

    Args:
        prompt (str): The prompt to display to the user.

    Returns:
        str: The user's input.
    """
    return input(prompt)